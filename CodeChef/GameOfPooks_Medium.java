import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
		// your code goes here
		Scanner input=new Scanner(System.in);
		int t=input.nextInt();
		StringBuilder sb=new StringBuilder();
		while(t-->0){
		    long n=input.nextLong();
		    if(n<=2) sb.append(1).append("\n");
		    else if(n==3) sb.append(n-1).append("\n");
		    else sb.append(n).append("\n");
		}
		System.out.print(sb);
	}
}
